public class h_Retangulo {
    public static void main(String[] args) {
        // Declaração das variáveis do retângulo
        double largura = 5.0;   // Largura do retângulo
        double altura = 3.0;    // Altura do retângulo

        // Cálculo da área e do perímetro
        double area = largura * altura;
        double perimetro = 2 * (largura + altura);

        // Exibição dos resultados no console
        System.out.println("Largura do Retângulo: " + largura);
        System.out.println("Altura do Retângulo: " + altura);
        System.out.println("Área do Retângulo: " + area);
        System.out.println("Perímetro do Retângulo: " + perimetro);
    }
}
