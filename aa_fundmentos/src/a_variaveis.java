// Exemplo simples de declaração e uso de variáveis em Java
// Cada tipo representa um dado diferente: inteiro, decimal, caractere e booleano
public class a_variaveis {
    public static void main(String[] args) {
        // Declaração de variáveis
        int idade = 25;           // idade inteira
        double salario = 3500.50; // valor decimal
        char genero = 'M';        // caractere único
        boolean ativo = true;     // verdadeiro ou falso

        // Exibição dos valores no console
        System.out.println("Idade: " + idade);
        System.out.println("Salário: " + salario);
        System.out.println("Gênero: " + genero);
        System.out.println("Ativo: " + ativo);
    }
}
