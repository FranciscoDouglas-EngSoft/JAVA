public class d_atribuicao {
    public static void main(String[] args) {
        // Declaração de variáveis
        int numero;

        // Atribuição de valor
        numero = 10;

        // Atribuição composta
        numero += 5;  // Equivalente a numero = numero + 5
        numero -= 3;  // Equivalente a numero = numero - 3
        numero *= 2;  // Equivalente a numero = numero * 2
        numero /= 4;  // Equivalente a numero = numero / 4
        numero %= 3;  // Equivalente a numero = numero % 3

        // Exibição do resultado final no console
        System.out.println("Valor final de número: " + numero);
    }
    
}
