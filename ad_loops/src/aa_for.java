// Exemplo simples de loop for em Java
// O loop for é usado para repetir um bloco de código várias vezes
// Neste exemplo, imprime os números de 1 a 15
public class aa_for {
    public static void main(String[] args){
        // Loop for: inicializa i em 1, repete enquanto i <= 15, incrementa i a cada volta
        for (int i = 1; i <= 15; i++) {
            // Imprime o valor atual de i
            System.out.println("Número: " + i);
        }
        // Mensagem final após o loop
        System.out.println("Fim do loop.");
    }
}
