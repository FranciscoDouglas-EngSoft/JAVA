public class bb_contagemDoces {
    public static void main(String[] args){
        // Inicializa o contador de doces
        int contadorDoces = 0;
        // Loop for: conta doces de 1 a 50
        for (int i = 1; i <= 50; i++) {
            contadorDoces++; // Incrementa o contador a cada iteração
        }
        // Imprime o total de doces contados
        System.out.println("Total de doces contados: " + contadorDoces);
    }
    
}
